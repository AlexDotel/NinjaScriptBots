//This namespace holds Strategies in this folder and is required. Do not change it. 
namespace NinjaTrader.NinjaScript.Strategies
{
	public class RandBuyEMA : Strategy
	{
		
		private EMA EMA1;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"Enter the description for your new custom Strategy here.";
				Name										= "RandBuyEMA";
				Calculate									= Calculate.OnBarClose;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				StartTime						= DateTime.Parse("09:30", System.Globalization.CultureInfo.InvariantCulture);
				TP					= 25;
				SL					= 33;
				Long					= true;
				PeriodEMA					= 200;
				Short					= true;
			}
			else if (State == State.Configure)
			{
				AddDataSeries("MES 06-25", Data.BarsPeriodType.Minute, 1, Data.MarketDataType.Last);
				AddDataSeries("MES 06-25", Data.BarsPeriodType.Day, 1, Data.MarketDataType.Last);
			}
			else if (State == State.DataLoaded)
			{				
				EMA1				= EMA(Closes[2], Convert.ToInt32(PeriodEMA));
				SetProfitTarget(@"BUY", CalculationMode.Ticks, TP);
				SetStopLoss(@"BUY", CalculationMode.Ticks, SL, false);
				SetProfitTarget(@"SELL", CalculationMode.Ticks, TP);
				SetStopLoss(@"SELL", CalculationMode.Ticks, SL, false);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 1
			|| CurrentBars[2] < 1)
				return;

			 // Set 1
			if ((Position.MarketPosition == MarketPosition.Flat)
				 && (Times[0][0].TimeOfDay == StartTime.TimeOfDay)
				 && (Closes[2][1] > EMA1[0])
				 && (Long == true))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), @"BUY");
			}
			
			 // Set 2
			if ((Position.MarketPosition == MarketPosition.Flat)
				 && (Times[0][0].TimeOfDay == StartTime.TimeOfDay)
				 && (Closes[2][1] < EMA1[0])
				 && (Short == true))
			{
				EnterShort(Convert.ToInt32(DefaultQuantity), @"SELL");
			}
			
		}

		#region Properties
		[NinjaScriptProperty]
		[PropertyEditor("NinjaTrader.Gui.Tools.TimeEditorKey")]
		[Display(Name="StartTime", Order=1, GroupName="Parameters")]
		public DateTime StartTime
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="TP", Order=2, GroupName="Parameters")]
		public int TP
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="SL", Order=3, GroupName="Parameters")]
		public int SL
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="Long", Description="Si es falso, abrira ventas.", Order=4, GroupName="Parameters")]
		public bool Long
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="PeriodEMA", Order=5, GroupName="Parameters")]
		public int PeriodEMA
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="Short", Order=6, GroupName="Parameters")]
		public bool Short
		{ get; set; }
		#endregion

	}
