	public class BollingerBreackout : Strategy
	{
		private bool CanTrade;
		private double StopLoss;
		private double TakeProfit;
		private double TargetSize;

		private Brush Brush1;
		private Bollinger Bollinger1;
		private Bollinger Bollinger2;
		
		private Bollinger Bollinger3;
		private Series<double> StopSizeSerie;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"Enter the description for your new custom Strategy here.";
				Name										= "BollingerBreackout";
				Calculate									= Calculate.OnBarClose;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				STime						= DateTime.Parse("15:30", System.Globalization.CultureInfo.InvariantCulture);
				Etime						= DateTime.Parse("17:30", System.Globalization.CultureInfo.InvariantCulture);
				Longs					= true;
				Shorts					= true;
				PauseTime						= DateTime.Parse("12:59", System.Globalization.CultureInfo.InvariantCulture);
				ResumeTime						= DateTime.Parse("13:59", System.Globalization.CultureInfo.InvariantCulture);
				BollingerPeriods					= 20;
				BollingerDev					= 2;
				RRR					= 2;
				MaxTradeLose					= -120;
				CanTrade					= false;
				StopLoss					= 0;
				TakeProfit					= 0;
				TargetSize					= 0;
			}
			else if (State == State.Configure)
			{
				Brush1 = new SolidColorBrush((Color)ColorConverter.ConvertFromString("#FF2D2D2F"));
				Brush1.Freeze();
			}
			else if (State == State.DataLoaded)
			{				
				StopSizeSerie = new Series<double>(this);			
				Bollinger1				= Bollinger(Close, BollingerDev, Convert.ToInt32(BollingerPeriods));
				Bollinger2				= Bollinger(Close, BollingerDev, Convert.ToInt32(BollingerPeriods));
				Bollinger3				= Bollinger(Close, 2, 20);
				Bollinger1.Plots[0].Brush = Brushes.Snow;
				Bollinger1.Plots[1].Brush = Brushes.Aqua;
				Bollinger1.Plots[2].Brush = Brushes.Snow;
				AddChartIndicator(Bollinger1);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			CanTrade = false;
			if (CurrentBars[0] < 2)
				return;

			 // Set 2
			if ((Times[0][0].TimeOfDay >= STime.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Etime.TimeOfDay))
			{
				CanTrade = true;
				BackBrush = Brush1;
			}
			
			 // Set 3
			if ((Times[0][0].TimeOfDay >= PauseTime.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= ResumeTime.TimeOfDay))
			{
				CanTrade = false;
				BackBrush = Brushes.RosyBrown;
			}
			
			 // Set 4
			if ((CanTrade == true)
				 && (Open[0] < Bollinger1.Upper[0])
				 && (Close[0] > Bollinger1.Upper[0])
				 && (Position.MarketPosition == MarketPosition.Flat))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), @"Long");
				StopLoss = Bollinger2.Middle[0];
				Draw.Line(this, @"BollingerBreackout Line_1 " + Convert.ToString(Close[0]), false, 0, StopLoss, 2, StopLoss, Brushes.Red, DashStyleHelper.Solid, 2);
				StopSizeSerie[0] = (Close[0] - (StopLoss)) ;
				TargetSize = (StopSizeSerie[0] * RRR) ;
				TakeProfit = (Close[0] + (TargetSize)) ;
				Draw.Line(this, @"BollingerBreackout Line_1 " + Convert.ToString(Close[0]) + " " + Convert.ToString(Close[0]), false, 0, TakeProfit, 2, TakeProfit, Brushes.Green, DashStyleHelper.Solid, 2);
				Draw.Line(this, @"BollingerBreackout Line_1 " + Convert.ToString(Close[0]) + " " + Convert.ToString(Close[0]) + " " + Convert.ToString(Close[0]), false, 0, TakeProfit, 2, TakeProfit, Brushes.Green, DashStyleHelper.Solid, 2);
			}
			
			 // Set 5
			if ((Position.MarketPosition == MarketPosition.Long)
				 // TakeProfit and StopLoss Cross
				 && ((CrossAbove(Close, TakeProfit, 1))
				 || (CrossBelow(Close, StopLoss, 1))))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), @"CloseLong", @"Long");
			}
			
			 // Set 6
			if ((Position.MarketPosition == MarketPosition.Long)
				 && (CrossBelow(Close, Bollinger3.Middle, 1)))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), @"CloseLong", @"Long");
			}
			
			 // Set 7
			if ((Position.MarketPosition == MarketPosition.Long)
				 && (Position.GetUnrealizedProfitLoss(PerformanceUnit.Currency, Close[0]) <= MaxTradeLose))
			{
				ExitLong(Convert.ToInt32(DefaultQuantity), @"CloseLong", @"Long");
			}
			
		}

		#region Properties
